package week1.day2;

public class ForLoopExample {

	public static void main(String[] args) {
		System.out.println("Increment Loop"); 
		for(int i=1;i<=10;i++) {
			System.out.print(i+" ");
		}
		
		System.out.println("\nDecrement Loop");
		for(int i=20 ;i>0;i--) {
			System.out.print(i+" ");
		}
	}

}
